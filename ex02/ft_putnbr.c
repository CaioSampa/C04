#include <unistd.h>

void	ft_putnbr(int nb)
{
    char n;
	if(nb == -2147483648)
    {
        write(1, "-2147483648", 12);
        return;
    }
    if(nb < 0)
    {
        write(1, "-", 1);
        nb *= -1;
    }
    if(nb >= 10)
        ft_putnbr(nb / 10);
    n = (nb % 10) + '0';
    write(1, &n, 1);
}

int main(void)
{
    ft_putnbr(123);
    return (0);
}
